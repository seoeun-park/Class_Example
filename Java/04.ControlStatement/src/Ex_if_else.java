public class Ex_if_else {
	public static void main(String[] args) {
		/*
		 ★ 블럭 if 문 : 조건을 판단하여 참일 때 또는 거짓일 때 명령을 실행
		 
		 if (조건식) {
		 	참일 때 실행되는 문장;
		 } else {
		 	거짓일 때 실행되는 문장;
		 }
		 
		 */
		
		int a = 9;
		
		if (a % 2 == 0) {
			System.out.println("짝수입니다.");		// 조건식이 true
		} else {
			System.out.println("홀수입니다.");		// 조건식이 false
		} //if
		
		
		
	} //main
} //class
